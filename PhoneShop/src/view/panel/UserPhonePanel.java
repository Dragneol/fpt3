    private void searchTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTxtKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            ArrayList<PhoneDTO> list = PhoneDAO.findLikeName(searchTxt.getText());
            showPhones(list);
        }
    }//GEN-LAST:event_searchTxtKeyPressed
    
    PhoneDTO retrivePhoneInfo() {
        float price;
        try {
            price = Float.parseFloat(priceTxt.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Invalid price format!");
            return null;
        }
        return new PhoneDTO(idTxt.getText(), nameTxt.getText(), osTxt.getText(), descriptionTxt.getText(), pictureLink, price);
    }
    
    void distrivePhoneInfo(PhoneDTO phone) {
        idTxt.setText(phone.getId());
        nameTxt.setText(phone.getName());
        osTxt.setText(phone.getOs());
        descriptionTxt.setText(phone.description);
        pictureLink = phone.picture;
        priceTxt.setText(String.format("%.2f", phone.getPrice()));
    }

