    private void showPhoneDetail() {
        int rowCount = phoneTable.getSelectedRow();
        if (rowCount == -1) return;
        PhoneDTO phone = PhoneDAO.getPhone((String) model.getValueAt(rowCount, 0));
        distrivePhoneInfo(phone);
        ImageLoader.smartAttaching(phone.getPicture(), detailImg);
    }
    
